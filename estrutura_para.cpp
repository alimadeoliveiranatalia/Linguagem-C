#include <stdio.h>
#include <stdlib.h>
int main(int argc, char *argv[]) {

/* Estrutura FOR*/	

	int i;
	printf("\n Estou contando");
	for (i=1;i<=10;i++){
		printf("\n %i",i);
	}		
	printf("\n Terminei de contar");

/* Estrutura WHILE */

i=1;
	while (i<=10){
		printf("\n %i",i);
		i = i+1;
		
	}	
}
 
